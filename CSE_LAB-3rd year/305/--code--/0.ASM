TITLE STACK_REVERSE_OR_STRING_REVERSE
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
LINE DB CR,LF,'$'
MSG1 DB 'INPUT : $'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,9
INT 21H

XOR CX,CX
MOV AH,1
INT 21H
FIRST:
	CMP AL,0DH
	JE FINISH
	PUSH AX
	INC CX
	INT 21H
	JMP FIRST
FINISH:
	LEA DX,LINE
	MOV AH,9
	INT 21H
	JCXZ EXIT
PRINT:
	POP DX
	MOV AH,2
	INT 21H
	LOOP PRINT
	
EXIT:

MOV AH,4CH
INT 21H

MAIN ENDP
	 END MAIN