TITLE  Toggle Case Conversion
.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER A LOWER CASE OR UPPER CASE LETTER : $"
CR EQU 0DH
LF EQU 0AH
MSG2 DB CR,LF,"EQUIVALENT TOGGLE CASE LETTER IS : $"

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG1
INT 21H


MOV AH,1
INT 21H

CMP AL,65
JL  EXIT
CMP AL,90
JLE LOWER_CASE

CMP AL,96
JL EXIT
CMP AL,122
JLE UPPER_CASE
JMP EXIT
LOWER_CASE:
	ADD AL,32
	MOV BL,AL
	JMP DISPLAY
UPPER_CASE:
	SUB AL,32
	MOV BL,AL
DISPLAY:
	MOV AH,9
	LEA DX,MSG2
	INT 21H
	MOV DL,BL
	MOV AH,2
	INT 21H
EXIT:
	MOV AH,4CH
	INT 21H

MAIN ENDP
	 END MAIN