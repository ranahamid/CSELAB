TITLE 
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

CALL MULTIPLY
MOV AH,4CH
INT 21H

MAIN ENDP


MULTIPLY PROC
XOR DX,DX
POP BX
POP AX
REPIT:
	TEST BX,1
	JZ EVEEN
	ADD DX,AX
EVEEN:
	SHL AX,1
	SHR BX,1
	JNZ REPIT
	PUSH DX
	RET

MULTIPLY ENDP
 END MAIN