TITLE 
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG1 DB 'INPUT NUMBER : $'
MSG2 DB 'OUTPUT NUMBER : $'
LINE DB CR,LF,'$'
.CODE
MAIN PROC 

MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG1
INT 21H

CALL INDEC
MOV CX,AX
MOV AX,1
TOP:
	MUL CX
	LOOP TOP
PUSH AX

MOV AH,9
LEA DX,LINE
INT 21H

LEA DX,MSG2
INT 21H

POP AX
CALL OUTDEC 
MOV AH,4CH
INT 21H

MAIN ENDP
INCLUDE 2.ASM
INCLUDE 3.ASM
	 END MAIN