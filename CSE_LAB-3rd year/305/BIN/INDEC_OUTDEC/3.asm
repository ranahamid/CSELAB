OUTDEC PROC

	OR AX,AX
	JGE FIRST
	PUSH AX
	MOV DL,'-'
	MOV AH,2
	INT 21H
	POP AX
	NEG AX

FIRST:
	XOR CX,CX;CX=NUMBER OF DIGIT
	MOV BX,10D
SEC:
	XOR DX,DX;HIGHER PART=0
	DIV BX;DX:AX=DX:AX/BX
	PUSH DX
	INC CX
	OR AX,AX
	JNE SEC
PRINT:
	MOV AH,2
	POP DX
	OR DL,30H
	INT 21H
	LOOP PRINT
	RET
OUTDEC ENDP
	
