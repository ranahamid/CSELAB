;sorting
TITLE SORTING
.MODEL SMALL
.STACK 100H
.DATA
W DB 2,3,1,5,4,6,9
.CODE
MAIN PROC

MOV AX,@DATA
MOV ES,AX
MOV DS,AX


LEA SI,W
MOV BX,7 ;NUMBER OF ELEMENTS


;PROC SORT
DEC BX
JE END_SORT

MOV DX,SI
SORT_LOP:
	MOV SI,DX
	MOV CX,BX
	MOV DI,SI
	MOV AL,[DI]
FIND_BIG:
	INC SI
	CMP [SI],AL
	JG NEXT
	MOV DI,SI
	MOV AL,[DI]

NEXT:
	LOOP FIND_BIG
	
	CALL SWAP;SI AND DI
	DEC BX
	JNE SORT_LOP
	
END_SORT:
	LEA SI,W
	MOV CX,7;NUMBER OF ELEMENTS
	JMP PRINT1
PRINT1:
	MOV DL,[SI]
	ADD DL,48
	ADD SI,1
	MOV AH,2
	INT 21H
	LOOP PRINT1
EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
;SWAP 

SWAP PROC

MOV AL,[SI]
XCHG AL,[DI]
MOV [SI],AL
RET
SWAP ENDP

END MAIN