TITLE FIND FIRST AND LAST CHAR
.MODEL SMALL
.STACK 100H
.DATA

CR EQU 0DH
LF EQU 0AH
MSG DB CR,LF,'ENTER A SENTECE TO FIND FIRST AND LAST CHAR.$'
CAP_MSG DB CR,LF,'FIRST CHAR IS: '
FIRST DB ']'
	   DB ' LAST CHAR IS: '
LAST DB '@ $'
NO_CAPITAL DB 'NO CAPITAL LETTER.$'
LINE DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG
INT 21H

LEA DX,LINE
INT 21H

MOV AH,1
INT 21H

LOOPING:
	CMP AL,0DH
	JE QUIT
	CMP AL,'A'
	JL INPUT
	CMP AL,'Z'
	JG INPUT
	
	CMP AL,FIRST
	JGE CHECK_LAST
	MOV FIRST,AL
	JMP INPUT
CHECK_LAST:
	CMP AL,LAST
	JLE INPUT
	MOV LAST,AL
	JMP INPUT	
	
INPUT:
	MOV AH,1
	INT 21H
	JMP LOOPING
	
QUIT:
	CMP FIRST,']'
	JE NO_CAP
	JNE DISPLAY_CAP
NO_CAP:
	LEA DX, NO_CAPITAL
	MOV AH,9
	INT 21H
	JMP EXIT
DISPLAY_CAP:
	LEA DX,CAP_MSG 
	MOV AH,9
	INT 21H
	JMP EXIT
EXIT:
	MOV AH,4CH
	INT 21H

MAIN ENDP
	 END MAIN