;string reverse using array
TITLE  REVERSE_STRING_array
.MODEL SMALL 
.STACK 100H
.DATA
MSG1 DB 'Enter WORD ','$'
LINE DB 0AH,0DH,'$'
MSG2 DB 100 DUP(?)
.CODE
MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG1
INT 21H
LEA DX,LINE
INT 21H

XOR BX,BX

MOV AH,1
INPUT:
	INT 21H
	CMP AL,0DH
	JE CHECK
	MOV MSG2[BX],AL
	INC BX
	JMP INPUT
CHECK:
	MOV AH,9
	LEA DX,LINE
	INT 21H
	MOV CX,BX
	JCXZ EXIT

MOV AH,2	
PRINT:
	DEC CX
	MOV DL,MSG2[CX]
	INT 21H
	LOOP PRINT

EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
     END MAIN
	
