TITLE PRIME_TEST
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'INPUT A VALUE: $'
MSG1 DB 'ODD$'
MSG2 DB 'EVEN$'
LINE DB 0AH,0DH,'$'
VAL1 DB ?
VAL2 DB ?
.CODE
MAIN  PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX


MOV AH,9
LEA DX,MSG
INT 21H


MOV AH,1
XOR CX,CX

INPUT:
	INT 21H
	CMP AL,0DH
	JZ PRINT_LINE
	INC CX
	PUSH AX
	JMP INPUT

PRINT_LINE:
	
	MOV AH,9
	LEA DX,LINE
	INT 21H
PRINT:
	
	POP DX
	MOV AH,2
	INT 21H
	LOOP PRINT

EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
	END MAIN