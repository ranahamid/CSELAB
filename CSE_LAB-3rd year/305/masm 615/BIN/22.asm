TITLE PRIME_TEST
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'INPUT A VALUE: $'
MSG1 DB 'ODD$'
MSG2 DB 'EVEN$'
LINE DB 0AH,0DH,'$'
STR1 DB 100 DUP(?)
VAL1 DB ?
VAL2 DB ?
W DB 2,5,7,7,4,1,8
.CODE
MAIN  PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX

LEA SI,W
MOV BX,7
MOV DX,SI

SORT:
	MOV SI,DX
	MOV CX,BX
	MOV DI,SI
	MOV AL,[DI]
FIND_BIG:
	INC SI
	CMP [SI],AL
	JNG NEXT
	MOV DI,SI
	MOV AL,[DI]
NEXT:
	LOOP FIND_BIG
	CALL SWAP
	DEC BX
	JNZ SORT

LEA SI,W
MOV CX,7
MOV AH,2

PRINT:
	MOV DL,[SI]
	ADD DL,30H
	INC SI
	INT 21H
	LOOP PRINT


EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP

SWAP PROC 

MOV AL,[DI]
XCHG AL,[SI]
MOV [DI],AL
RET
 SWAP ENDP
	END MAIN