TITLE PRIME_TEST
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'INPUT A CHAR: $'
LINE DB 0AH,0DH,'$'
VAL DB ?
.CODE
MAIN  PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX

MOV AH,9
LEA DX,MSG
INT 21H


MOV AH,1
INT 21H

MOV VAL,AL

MOV AH,9
LEA DX,LINE
INT 21H
MOV AL,VAL

CMP AL,65
JL R2
CMP AL,90
JLE R1
CMP AL,97
JL R2
CMP AL,122
JLE R1
JMP R2

R1:
	XOR AL,32
	MOV AH,2
	MOV DL,AL
	INT 21H
	JMP EXIT
R2:
	MOV AH,2
	MOV DL,AL
	INT 21H

EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
	END MAIN