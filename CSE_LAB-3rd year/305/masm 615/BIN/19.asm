TITLE PRIME_TEST
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'INPUT A VALUE: $'
LINE DB 0AH,0DH,'$'
VAL1 DB ?
VAL2 DB ?
.CODE
MAIN  PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX


MOV AH,9
LEA DX,MSG
INT 21H


MOV AH,1
INT 21H
SUB AL,30H
MOV VAL1,AL

MOV AH,9
LEA DX,LINE
INT 21H

MOV CH,00H
MOV CL,VAL1
MOV AX,1
TOP:
	MUL CX
	LOOP TOP

ADD AX,30H
MOV DX,AX
MOV AH,2
INT 21H


EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
	END MAIN