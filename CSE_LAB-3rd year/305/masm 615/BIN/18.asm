TITLE PRIME_TEST
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'INPUT A VALUE: $'
LINE DB 0AH,0DH,'$'
VAL1 DB ?
VAL2 DB ?
.CODE
MAIN  PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX


MOV AH,9
LEA DX,MSG
INT 21H


MOV AH,1
INT 21H
SUB AL,30H
MOV VAL1,AL

MOV AH,9
LEA DX,LINE
INT 21H


MOV AH,1
INT 21H
SUB AL,30H
MOV VAL2,AL

MOV BL,VAL2
MOV AL,VAL1
MOV AH,00
DIV BL
MOV CL,AH

ADD AL,30H
MOV DL,AL
MOV AH,2
INT 21H


ADD CL,30H
MOV DL,CL
MOV AH,2
INT 21H

EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
	END MAIN