TITLE PRIME_TEST
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'INPUT A VALUE: $'
LINE DB 0AH,0DH,'$'
VALUE DB ?
N_P DB 'NOT PRIME $'
y_P DB 'PRIME $'

.CODE
MAIN  PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX

MOV AH,9
LEA DX,MSG
INT 21H

MOV AH,1
INT 21H
ADD AL,30H
MOV VALUE,AL

MOV AH,9
LEA DX,LINE
INT 21H

MOV AH,00H
MOV AL,VALUE
MOV CL,2
DIV CL
MOV CL,AL

TEST1:	
	MOV AH,00H
	MOV AL,VALUE
	DIV CL
	CMP AH,0
	JZ NOT_PR
	DEC CL
	CMP CL,1
	JNE TEST1
	JMP PRIME
NOT_PR:
	MOV AH,9
	LEA DX,N_P
	INT 21H
	JMP EXIT

PRIME:

	MOV AH,9
	LEA DX,Y_P
	INT 21H
	

EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP
	END MAIN