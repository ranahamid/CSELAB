TITLE PRIME_TEST
.MODEL SMALL
.STACK 100H
.DATA
MSG DB 'INPUT A VALUE: $'
MSG1 DB 'ODD$'
MSG2 DB 'EVEN$'
LINE DB 0AH,0DH,'$'
STR1 DB 100 DUP(?)
VAL1 DB ?
VAL2 DB ?
W DB 2,5,7,7,4,1,8
.CODE
MAIN  PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX

LEA DI,W
CALL IN_STR;BX=nO. OF CHAR
MOV VAL1,BL

MOV AH,9
LEA DX,LINE 
INT 21H

LEA SI,W
MOV DX,SI


SORT_LOP:
	MOV SI,DX
	MOV CX,BX
	MOV DI,SI
	MOV AL,[DI]
FIND_BIG:
	INC SI
	CMP [SI],AL
	JG NEXT
	MOV DI,SI
	MOV AL,[DI]

NEXT:
	LOOP FIND_BIG
	CALL SWAP
	DEC BX
	JNE SORT_LOP

LEA SI,W
MOV CL,VAL1
MOV AH,2

PRINT:
	MOV DL,[SI]
	INC SI
	INT 21H
	LOOP PRINT


EXIT:
	MOV AH,4CH
	INT 21H
MAIN ENDP

SWAP PROC 

MOV AL,[DI]
XCHG AL,[SI]
MOV [DI],AL
RET
 SWAP ENDP
IN_STR PROC

XOR BX,BX
MOV AH,1
CLD
INPUT:
	INT 21H
	CMP AL,0DH
	JZ EXIT1
	INC BX
	STOSB
	JMP INPUT
EXIT1:
	RET

IN_STR ENDP
	END MAIN