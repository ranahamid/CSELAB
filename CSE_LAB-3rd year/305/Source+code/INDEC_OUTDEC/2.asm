INDEC PROC
BEGAIN:
	XOR BX,BX ;BX=TOTAL
	XOR CX,CX ;CX=SIGN
	MOV AH,1
	INT 21H
	
	CMP AL,'-'
	JE MINUS
	CMP AL,'+'
	JE PLUS
	JMP REGULAR
MINUS:
	MOV CX,1
PLUS:
	INT 21H
REGULAR:
	CMP AL,'0'
	JNGE NOT_DIGIT
	CMP AL,'9'
	JNLE NOT_DIGIT
	
	AND AX,000FH; NOT USE THIS ===ADD AX,30H ;	
				;HIGHER PART OF AX IS NOT ZERO IF WE USE SUB
	PUSH AX
	
	MOV AX,10
	MUL BX; DX:AX=AX*BX
	POP BX 
	ADD BX,AX
	
	
	MOV AH,1
	INT 21H
	CMP AL,0DH
	JNE REGULAR
	
	MOV AX,BX
	OR CX,CX
	JE EXIT
	NEG AX
EXIT:
	RET
NOT_DIGIT:
	MOV AH,2
	MOV DL,0DH
	INT 21H
	MOV DL,0AH
	INT 21H
	JMP BEGAIN
INDEC ENDP 
