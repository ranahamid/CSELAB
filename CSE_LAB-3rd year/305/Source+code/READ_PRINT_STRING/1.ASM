TITLE 
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG1 DB 'INPUT STRING : $'
MSG2 DB 'OUTPUT STRING IS : $'
STRING DB 80 DUP (0)
LINE DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX

LEA DX,MSG1
MOV AH,9 
INT 21H

LEA DI,STRING
CALL READ_STR

LEA DX,LINE
MOV AH,9
INT 21H

LEA DX,MSG2
MOV AH,9 
INT 21H

LEA SI,STRING
CALL DISP_STR

MOV AH,4CH
INT 21H

MAIN ENDP
INCLUDE 2.ASM
INCLUDE 3.ASM
	 END MAIN