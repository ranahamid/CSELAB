TITLE CASE_CONVERSION
.MODEL SMALL
.STACK 100H
.DATA 
CR EQU 0DH
LF EQU 0AH
MSG1  DB 'INSERT A LOWER CASE LETTER...$'
MSG2  DB 'UPPER CASE LETTER IS : $'


.CODE 
MAIN PROC

MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,20H
MOV BL,AL

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H

MOV AH,9
LEA DX,MSG2
INT 21H

MOV AH,2
MOV DL,BL 
INT 21H


MOV AH,4CH
INT 21H
MAIN ENDP
     END MAIN
