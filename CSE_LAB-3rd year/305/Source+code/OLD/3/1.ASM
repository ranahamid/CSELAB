TITLE IF_CAPITAL_LETTER_THEN_DISPLAY
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,1
INT 21H
CMP AL,65
JL EXIT
CMP AL,90
JLE DISPLAY
JMP EXIT

DISPLAY:
	MOV AH,2
	MOV DL,AL
	INT 21H

EXIT:
MOV AH,4CH
INT 21H

MAIN ENDP
	 END MAIN