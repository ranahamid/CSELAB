TITLE STRING_REVERS
.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'INPUT A STRING    : $'
MSG2 DB 'REVERSE STRING IS : $'
CR EQU 0DH
LF EQU 0AH
LINE DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG1
INT 21H

MOV AH,1
XOR CX,CX;MOV CX,0;AND CX,0
INT 21H

INPUT:
	CMP AL,0DH
	JZ NEXT ;JE
	PUSH AX
	INC CX  
	INT 21H
	JMP INPUT
	
NEXT:
	JCXZ EXIT
	
	MOV AH,9
	LEA DX,LINE
	INT 21H
	LEA DX,MSG2
	INT 21H
	JMP PRINT
PRINT:

	POP DX
	MOV AH,2
	INT 21H
	;LOOP PRINT ;
	DEC CX;
	JNZ PRINT
	
EXIT:
    MOV AH,4CH
	INT 21H
	
	MAIN ENDP
		 END MAIN