TITLE BIG NUMBER 
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AH,1
INT 21H
MOV BL,AL
INT 21H
CMP AL,BL
JG BIG_AL
JLE BIG_BL
BIG_BL:
	JMP DISPLAY
BIG_AL:
	MOV BL,AL
DISPLAY:
	MOV AH,9
	LEA DX,MSG
	INT 21H
	MOV DL,BL
	MOV AH,2
	INT 21H

MOV AH,4CH
INT 21H


MAIN ENDP
	 END MAIN