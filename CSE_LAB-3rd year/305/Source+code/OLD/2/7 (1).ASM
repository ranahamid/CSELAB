TITLE DISPLAY_SMALL ASCII CODE
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AL,'B'
MOV BL,'a'

CMP AL,BL
JLE DIS_AL
JG  DIS_BL

DIS_AL:
	MOV DL,AL
	JMP	DISPLAY
DIS_BL:
	MOV DL,BL
DISPLAY:
	MOV AH,2
	INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
	 END MAIN