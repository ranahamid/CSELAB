TITLE  Toggle Case Conversion
.MODEL SMALL
.STACK 100H
.DATA

.CODE

MAIN PROC

MOV AH,1
INT 21H

CMP AL,65
JL  EXIT
CMP AL,90
JLE LOWER_CASE

CMP AL,96
JL EXIT
CMP AL,122
JLE UPPER_CASE
JMP EXIT
LOWER_CASE:
	ADD AL,32
	JMP DISPLAY
UPPER_CASE:
	SUB AL,32
DISPLAY:
	MOV DL,AL
	MOV AH,2
	INT 21H
EXIT:
	MOV AH,4CH
	INT 21H

MAIN ENDP
	 END MAIN