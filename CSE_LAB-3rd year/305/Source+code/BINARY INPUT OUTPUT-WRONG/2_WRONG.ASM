;BINARY INPUT OUTPUT


TITLE 
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
LINE DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

XOR BX,BX
MOV AH,1
INT 21H	
MOV CX,16
FIRST:
	CMP AL,0DH
	JE LINE_IN
	AND AL,0FH
	SHL BX,1
	OR BL,AL
	INT 21H
	JMP FIRST
LINE_IN:
	MOV AH,9
	LEA DX,LINE
	INT 21H
	JMP FINISH_INPUT

FINISH_INPUT:
	ROL BX,1
	JC PRINT_1
	JNC PRINT_0
	CMP CX,0
	JZ EXIT
	LOOP FINISH_INPUT
	
PRINT_1:
	MOV DL,'1'
	MOV AH,2
	INT 21H
	RET
PRINT_0:
	MOV DL,'0'
	MOV AH,2
	INT 21H
	RET
EXIT:
MOV AH,4CH
INT 21H

MAIN ENDP
	 END MAIN