;COUNT VOWEL AND CONSTANTS
TITLE 
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
LINE DB CR,LF,'$'
STRING DB 80 DUP(0)
VOW DB 'AEIOU'
SPACE DB ' '
CONS DB 'BCDFGHJKLMNPQRSTVWXYZ'
DIGIT DB '0123456789'
VMSG DB CR,LF,'VOWELS: $'
CMSG DB CR,LF,'CONSTANTS: $'
DMSG DB CR,LF,'DIGITS: $'
SMSG DB CR,LF,'SPACE: $'

VCOUNT DW 0
CCOUNT DW 0
SCOUNT DW 0
DCOUNT DW 0
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX

LEA DI,STRING
CALL READ_STR;BX=NO OF CHAR
MOV SI,DI
CLD

FIRST2:
	LODSB 
	LEA DI,VOW
	MOV CX,5
	REPNE SCASB
	JNE CHECK_CONS
	INC VCOUNT
	JMP LAST2
CHECK_CONS:
	LEA DI,CONS
	MOV CX,21
	REPNE SCASB
	JNE CHK_DIG
	INC CCOUNT
	JMP LAST2
CHK_DIG: 
	LEA DI,DIGIT
	MOV CX,10
	REPNE SCASB
	JNE CHK_SPACE
	INC DCOUNT
	JMP LAST2
CHK_SPACE:
	LEA DI,SPACE
	MOV CX,1
	REPNE SCASB
	JNE LAST2
	INC SCOUNT
LAST2:
	DEC BX
	JNE FIRST2

MOV AH,9
LEA DX,VMSG
INT 21H
MOV AX,VCOUNT
CALL OUTDEC 

MOV AH,9
LEA DX,CMSG
INT 21H
MOV AX,CCOUNT
CALL OUTDEC 

MOV AH,9
LEA DX,DMSG
INT 21H
MOV AX,DCOUNT
CALL OUTDEC


MOV AH,9
LEA DX,SMSG
INT 21H
MOV AX,SCOUNT
CALL OUTDEC

	MOV AH,4CH
	INT 21H

MAIN ENDP
OUTDEC PROC
	;AX=NUMBER

	XOR CX,CX;CX=NUMBER OF DIGIT
	MOV BX,10D
SEC:
	XOR DX,DX;HIGHER PART=0
	DIV BX;DX:AX=DX:AX/BX
	PUSH DX
	INC CX

	OR AX,AX
	JNE SEC
PRINT:
	MOV AH,2
	POP DX
	OR DL,30H
	INT 21H
	LOOP PRINT

	RET
OUTDEC ENDP

READ_STR PROC

PUSH DI

CLD
XOR BX,BX
MOV AH,1
INT 21H

READ:
	CMP AL,0DH
	JE EXIT
	CMP AL,8H
	JNE STORE
	
	DEC DI
	DEC BX
	JMP INPUT
STORE:
	STOSB
	INC BX
INPUT:
	MOV AH,1
	INT 21H
	JMP READ
EXIT:
POP DI

RET
READ_STR  ENDP 

	 END MAIN