TITLE  Toggle Case Conversion
.MODEL SMALL
.STACK 100H
.DATA
LEN DW  0
MSG1 DB "ENTER  LOWER CASE OR UPPER CASE  : $"

CR EQU 0DH
LF EQU 0AH
STRING DB  80 DUP (0)
MSG2 DB CR,LF,"EQUIVALENT TOGGLE CASE  IS : $"
MSG DB 100 DUP(0)
LINE DB CR,LF,'$'
.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX
MOV ES,AX

MOV AH,9
LEA DX,MSG1
INT 21H

LEA DI,STRING
CALL READ_STR;BX=NO OF CHAR

MOV LEN,BX
CLD

LEA DX,LINE
MOV AH,9
INT 21H



LEA SI,STRING
MOV CX,LEN;NUMBER OF ELEMENTS

PRINT1:
	MOV DL,[SI]

	ADD SI,1
	MOV AH,2
	INT 21H
	LOOP PRINT1
EXIT:
	MOV AH,4CH
	INT 21H


	
MAIN ENDP
;read string proc
READ_STR PROC


CLD
XOR BX,BX
MOV AH,1
INT 21H

READ:
	CMP AL,0DH
	JE EXIT1
	XOR AL,20H    
	STOSB
	INC BX
INPUT:
	MOV AH,1
	INT 21H
	JMP READ
EXIT1:

RET
READ_STR  ENDP 
	 END MAIN